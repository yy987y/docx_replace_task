def build_batch_user_prompt(batch_requests: list[dict]) -> str:
    payload = json.dumps({"items": batch_requests}, ensure_ascii=False, indent=2)
    return textwrap.dedent(
        f"""
        请严格依据系统提示词，为下面每个环节单独生成新的观察案例。

        输出要求：
        1. 返回一个 JSON 对象，结构必须是：
           {{"items":[{{"index":0,"secondary":"...","blue":"..."}}]}}
        2. `items` 的顺序和输入完全一致，`index` 必须原样带回。
        3. 生成顺序必须是：先根据 `level1` 和 `source_hint` 提炼 `secondary`，再根据 `secondary` 展开 `blue`。
        4. `secondary` 只写“小目标”，不要句号，不要环节名，不要解释。
        5. `secondary` 优先写成 8-14 个字的自然观察点，最多不超过 18 个字。
        6. 不要把 `secondary` 写成 4-6 个字的口号式短句，除非一级标签内容本身只能支持这么短。
        7. `secondary` 要具体到一个可观察动作或行为，如“愿意把小车递给同伴”“看见碎屑会捡回盘里”，不要只写“继续尝试”“自己整理”这种太空的标签。
        8. `blue` 只写一段正文，不要换行，不要解释，不要 markdown。
        9. `blue` 的核心事件必须围绕你刚生成的 `secondary` 来写，不能脱离 `secondary` 另起主题。
        10. 每个 item 独立生成，不要在不同 item 之间复用句子骨架。
        11. `source_hint` 是当前一级标签下已有的原始内容或场景提示。你必须先理解它，再生成新的 `secondary`。
        12. 不能照抄 `source_hint`，不能只替换几个词，必须保留其事件方向并重写成新的现场片段。
        13. 如果 `source_hint` 为空，就只根据环节、年龄段、名字、材料和字数要求独立生成。
        14. 若 `needs_response_care` 为 true，`blue` 结尾必须补一句“通过……支持/帮助/引导幼儿……”
        15. 不要展示内部逻辑大纲。

        待处理数据：
        {payload}
        """
    ).strip()
